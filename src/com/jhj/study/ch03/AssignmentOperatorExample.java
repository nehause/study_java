package com.jhj.study.ch03;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		
		// +=
		
		int result1 = 0;
		int result2 = 0;
		int result3 = 1;
		
		result1 += 10;
		// = result = result + 10;
		
		System.out.println("result : " + result1);
		
		//-=
		result2 -= 10;
		// = result = result - 5;
		
		System.out.println("result : " + result2);
		
		result3 *= 10;
		// = result = result * 10;
		
		System.out.println("result : " + result3);

	}

}
