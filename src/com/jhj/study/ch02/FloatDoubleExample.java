package com.jhj.study.ch02;

public class FloatDoubleExample {

	public static void main(String[] args) {
		
		
		double var1 = 3.14;
		
		//float은 무조건 뒤에 f를 넣어야한다.
		//float var2 = 3.14;
		float var3 = 3.14f;
		float var4 = 3.14F;
		
		System.out.println(var1);
		System.out.println(var3);
		System.out.println(var4);

	}

}
